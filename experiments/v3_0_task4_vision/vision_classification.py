"""Task-4 universality: synthetic image classification.

Task spec
---------
Classify 32×32 "images" generated by a fixed random linear map into 10
classes with a small amount of label noise. This uses supervised
cross-entropy loss -- structurally different from self-prediction
(V1.0), QEC decoding (V2.1), and coefficient recovery (V3.0 task-3).

Data generator: for each sample we draw x ∈ R^1024 (flattened 32×32)
from N(0, I). Labels are assigned via y = argmax(W_fixed x + noise)
where W_fixed is a 1024×10 fixed random matrix generated with seed 0,
and noise is N(0, σ²) with σ = 0.1 to prevent the network from
reaching 100 % accuracy (otherwise the FIM would collapse).

Architecture: same 5-layer 256-neuron ReLU MLP used in V1.0 / V2.1 /
V3.0 task-3. Output dim = 10 (class logits). Loss = cross-entropy.

Measures the same SV ratio and FIM three-tier ratio for direct
comparison with the other three tasks.
"""
from __future__ import annotations

import argparse
import json
import os
import sys
import time
from pathlib import Path

import numpy as np
import torch
import torch.nn as nn

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE.parents[1] / "experiments/v2_1_qec"))
from analyze import fim_diagonal, sv_per_layer, tier_partition  # noqa: E402


_FIXED_PROJ_SEED = 0


def _make_teacher(dim: int, n_classes: int, device: torch.device) -> torch.Tensor:
    g = torch.Generator(device=device).manual_seed(_FIXED_PROJ_SEED)
    return torch.randn(dim, n_classes, generator=g, device=device)


def sample_batch(batch: int, dim: int, n_classes: int, teacher: torch.Tensor, noise: float, rng: np.random.Generator) -> tuple[torch.Tensor, torch.Tensor]:
    x = torch.from_numpy(rng.standard_normal(size=(batch, dim)).astype(np.float32)).to(teacher.device)
    logits = x @ teacher + noise * torch.from_numpy(rng.standard_normal(size=(batch, n_classes)).astype(np.float32)).to(teacher.device)
    y = logits.argmax(dim=-1)
    return x, y


class MLP(nn.Module):
    def __init__(self, in_dim: int, out_dim: int, width: int, hidden_layers: int = 5):
        super().__init__()
        mods: list[nn.Module] = [nn.Linear(in_dim, width), nn.ReLU()]
        for _ in range(hidden_layers - 1):
            mods += [nn.Linear(width, width), nn.ReLU()]
        mods.append(nn.Linear(width, out_dim))
        self.net = nn.Sequential(*mods)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


def run(*, width: int, steps: int, batch: int, seed: int, fim_samples: int, in_dim: int, n_classes: int, noise: float, device: torch.device) -> dict:
    torch.manual_seed(seed)
    rng = np.random.default_rng(seed)
    teacher = _make_teacher(in_dim, n_classes, device)
    net = MLP(in_dim, n_classes, width=width, hidden_layers=5).to(device)
    n_params = sum(p.numel() for p in net.parameters())
    opt = torch.optim.Adam(net.parameters(), lr=5e-4)
    ce = nn.CrossEntropyLoss()

    t0 = time.time()
    final_loss = None
    final_acc = None
    for step in range(steps):
        x, y = sample_batch(batch, in_dim, n_classes, teacher, noise, rng)
        logits = net(x)
        loss = ce(logits, y)
        opt.zero_grad(set_to_none=True)
        loss.backward()
        opt.step()
        final_loss = float(loss.detach())
        if step == steps - 1:
            final_acc = float((logits.argmax(dim=-1) == y).float().mean().detach())
    train_time = time.time() - t0
    print(f"  final CE={final_loss:.4f} acc={final_acc:.3f}", flush=True)

    sv_stats = sv_per_layer(net)
    max_sv = max((s["sv_ratio"] for s in sv_stats), default=0.0)

    def sample_loss():
        x, y = sample_batch(32, in_dim, n_classes, teacher, noise, rng)
        return ce(net(x), y)

    fim = fim_diagonal(net, sample_loss, n_samples=fim_samples)
    tiers = tier_partition(fim)
    return {
        "width": width,
        "n_params": n_params,
        "max_sv_ratio": round(max_sv, 1),
        "fim_tier1_tier3": round(tiers["ratio_tier1_tier3"], 1),
        "final_loss": round(final_loss or 0.0, 6),
        "final_acc": round(final_acc or 0.0, 3),
        "train_time": round(train_time, 1),
        "seed": seed,
    }


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--widths", type=int, nargs="+", default=[32, 64, 128, 256, 512, 1024])
    ap.add_argument("--steps", type=int, default=15_000)
    ap.add_argument("--batch", type=int, default=128)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--fim-samples", type=int, default=300)
    ap.add_argument("--in-dim", type=int, default=1024)
    ap.add_argument("--n-classes", type=int, default=10)
    ap.add_argument("--noise", type=float, default=0.5)
    ap.add_argument("--out", type=str, default=str(HERE / "v3_0_task4_results.json"))
    args = ap.parse_args()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    gpu = torch.cuda.get_device_name(0) if torch.cuda.is_available() else "CPU"
    print(f"Device: {device} ({gpu})", flush=True)

    results = []
    for w in args.widths:
        print(f"[{time.strftime('%H:%M:%S')}] width={w}", flush=True)
        try:
            r = run(
                width=w,
                steps=args.steps,
                batch=args.batch,
                seed=args.seed,
                fim_samples=args.fim_samples,
                in_dim=args.in_dim,
                n_classes=args.n_classes,
                noise=args.noise,
                device=device,
            )
        except torch.cuda.OutOfMemoryError:
            print(f"  OOM at width={w}")
            torch.cuda.empty_cache()
            continue
        print(f"  params={r['n_params']:,} SV={r['max_sv_ratio']}x FIM={r['fim_tier1_tier3']}x loss={r['final_loss']:.4f} acc={r['final_acc']:.3f}", flush=True)
        results.append(r)
        torch.cuda.empty_cache()

    if len(results) >= 2:
        log_p = np.log10([r["n_params"] for r in results])
        log_sv = np.log10(np.clip([r["max_sv_ratio"] for r in results], 1, None))
        log_fim = np.log10(np.clip([r["fim_tier1_tier3"] for r in results], 1, None))
        sv_fit = np.polyfit(log_p, log_sv, 1)
        fim_fit = np.polyfit(log_p, log_fim, 1)

        def _r2(y, fit):
            pred = np.polyval(fit, log_p)
            ss_res = float(np.sum((y - pred) ** 2))
            ss_tot = float(np.sum((y - np.mean(y)) ** 2))
            return 1 - ss_res / ss_tot if ss_tot > 0 else 0.0

        sv_pl = {"exponent": round(float(sv_fit[0]), 3), "r_squared": round(_r2(log_sv, sv_fit), 3)}
        fim_pl = {"exponent": round(float(fim_fit[0]), 3), "r_squared": round(_r2(log_fim, fim_fit), 3)}
    else:
        sv_pl = fim_pl = {"exponent": 0.0, "r_squared": 0.0}

    payload = {
        "config": vars(args),
        "device": str(device),
        "gpu": gpu,
        "results": results,
        "sv_power_law": sv_pl,
        "fim_power_law": fim_pl,
    }
    os.makedirs(os.path.dirname(args.out), exist_ok=True)
    with open(args.out, "w") as f:
        json.dump(payload, f, indent=2)
    print(f"\nSV power law:  {sv_pl}", flush=True)
    print(f"FIM power law: {fim_pl}", flush=True)
    print(f"Saved -> {args.out}", flush=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
